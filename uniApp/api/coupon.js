import Network from '../utils/network.js'
export default {
    getList(data,success,fail) {
		Network.setGetMessage('coupon',data,'加载中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
    },
	getUserList(data,success,fail) {
		Network.setGetMessage('coupon/user',data,'加载中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
	count(success,fail){
		Network.setGetMessage('coupon/user/count',{},'加载中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
	create(data,success,fail) {
		Network.setPostMessage('coupon',data,'处理中', function (res) {
		  success(res)
		}, function (res) {
		  uni.showToast({
			title: res.message,
			icon: 'none',
			duration: 2000
		  })
		})
	},
};
